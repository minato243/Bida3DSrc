bool AppDelegate::applicationDidFinishLaunching() {
    FileUtils::getInstance()->setEnableEncryptPath(fr::NativeService::getFolderUpdateAssetsById("demo"));
	FileUtils::getInstance()->addSearchPath("res");
    // initialize director
    auto director = Director::getInstance();
    auto glview = director->getOpenGLView();
    if(!glview) {
#if (CC_TARGET_PLATFORM == CC_PLATFORM_WIN32) || (CC_TARGET_PLATFORM == CC_PLATFORM_MAC) || (CC_TARGET_PLATFORM == CC_PLATFORM_LINUX)
        glview = GLViewImpl::createWithRect("PreviewAnimation", cocos2d::Rect(0, 0, 873, 480));
#else
        glview = GLViewImpl::create("PreviewAnimation");
#endif
        director->setOpenGLView(glview);
    }

    // turn on display FPS
    director->setDisplayStats(true);

    // set FPS. the default value is 1.0/60 if you don't call this
    director->setAnimationInterval(1.0f / 60);
    
    Size frameSize = glview->getFrameSize();
    float ratio = frameSize.width/frameSize.height;
    if(ratio < 2){
        glview->setDesignResolutionSize(1136, 640, ResolutionPolicy::FIXED_HEIGHT);
    }else{
        glview->setDesignResolutionSize(1400, 700, ResolutionPolicy::SHOW_ALL);
    }
    // Set the design resolution
	

    register_all_packages();


	GameMgr::getInstance()->start();
	//GameScene::view();

    return true;
}

// This function will be called when the app is inactive. Note, when receiving a phone call it is invoked.
void AppDelegate::applicationDidEnterBackground() {
    Director::getInstance()->stopAnimation();

#if USE_AUDIO_ENGINE
    AudioEngine::pauseAll();
#elif USE_SIMPLE_AUDIO_ENGINE
    SimpleAudioEngine::getInstance()->pauseBackgroundMusic();
    SimpleAudioEngine::getInstance()->pauseAllEffects();
#endif
}

// this function will be called when the app is active again
void AppDelegate::applicationWillEnterForeground() {
    Director::getInstance()->startAnimation();

#if USE_AUDIO_ENGINE
    AudioEngine::resumeAll();
#elif USE_SIMPLE_AUDIO_ENGINE
    SimpleAudioEngine::getInstance()->resumeBackgroundMusic();
    SimpleAudioEngine::getInstance()->resumeAllEffects();
#endif
}
